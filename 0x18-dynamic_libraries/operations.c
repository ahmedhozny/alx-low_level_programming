#include "operations.h"

/**
* add - adds two integers
* @a: first integer
* @b: second integer
* Return: the sum
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtracts two integers
* @a: first integer
* @b: second integer
* Return: the difference
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiplies two integers
* @a: first integer
* @b: second integer
* Return: the product
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divides two integers
* @a: first integer
* @b: second integer
* Return: the dividend
*/
int div(int a, int b)
{
	return (a / b);
}

/**
  mod - finds the modulus of two integers
  @a: first integer
  @b: second integer
  Return: the remainder of division
*/
int mod(int a, int b)
{
	return (a % b);
}
